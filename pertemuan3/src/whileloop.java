public class whileloop {
    public static void main(String[] args) {
        
        System.out.println("Perulangan dengan while");
        int cx = 0;
        int batas = 10;
        
        while(cx < batas){
            System.out.println("Perulangan ke-"+cx );
            cx++;
          
        }
        
    }
}
