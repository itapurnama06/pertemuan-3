public class dowhileloop {
    public static void main(String[] args){
        int cx = 0;
        int batas = 10;
        
        System.out.println("Perulangan Do while");
        do{
            
            System.out.println("Perulangan ke-"+cx);
            cx++;
            
        }while (cx < batas);
    }
    
}
